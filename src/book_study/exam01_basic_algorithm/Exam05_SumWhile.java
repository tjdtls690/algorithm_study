package book_study.exam01_basic_algorithm;

public class Exam05_SumWhile {
	public static void main(String[] args) {
		
		int sum = 0;
		int i = 1;
		int g = 0;
		while(i <= 10) {
			sum += i;
			i++;
			g = i;
		}
		System.out.println(sum);
		System.out.println(10*11/2);
		System.out.println(g);
		
	}

}
